			<div class="sidebar">
			
				<h2>Background</h2>
				
				<p><strong>OpenAccessPhilly</strong> is a group of Philadelphians working at the intersection of technology, innovation, and civic participation.  Launched in August 2011, membership is diverse, with people coming from city government, large and small companies, non-profits, grant making foundations, and interested non-Philadelphia groups.</p>
				
				<p>Our vision is to connect the passion and both new &amp; existing projects of Philadelphians to make local government more open, transparent, and participatory through three main areas of focus:</p>
				
				<ul>
					<li><strong>Citizen Engagement</strong> <br>
					Providing Philadelphians with access to the data, information and applications needed to give any citizen an ability to advance meaningful change in their communities.</li>
					
					<li><strong>Digital Inclusion</strong> <br>
					Supporting infrastructure, connectivity, and content needs to give all Philadelphians the opportunity to connect to our digital future</li>
					
					<li><strong>Infotec Economic Development</strong> <br>
					Removing roadblocks, providing incentives and connecting professionals in the information &amp; technology sectors to benefit employment, revenue, productivity and innovation.</li>
				</ul>

